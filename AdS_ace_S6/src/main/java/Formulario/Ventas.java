    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        // TODO add your handling code here:

        String terminoBusqueda = busquedaTextField.getText().toLowerCase();

        DefaultTableModel modelo = (DefaultTableModel) productosTable.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(modelo);
        productosTable.setRowSorter(sorter);

        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + terminoBusqueda));
    }//GEN-LAST:event_buscarButtonActionPerformed

