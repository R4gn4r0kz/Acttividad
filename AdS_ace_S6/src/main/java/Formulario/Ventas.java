    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed

        double total = 0.0;
        DefaultTableModel carritTable = (DefaultTableModel) carritoTable.getModel();

        // Calcula el total sumando los precios de los productos en el carrito
        for (int i = 0; i < carritoTable.getRowCount(); i++) {
            // Obtener y validar el precio
            Object precioObj = carritoTable.getValueAt(i, 2);
            double precio = (precioObj != null) ? ((Number) precioObj).doubleValue() : 0.0;

            // Obtener y validar la cantidad
            Object cantidadObj = carritoTable.getValueAt(i, 3);
            int cantidad = (cantidadObj != null) ? ((Number) cantidadObj).intValue() : 0;

            total += precio * cantidad;
        }

        // Mostrar el total al usuario
        JOptionPane.showMessageDialog(this, "El total a pagar es: $" + String.format("%.2f", total));

        int opcion = JOptionPane.showConfirmDialog(this, "¿Desea proceder con el pago?", "Confirmación de Pago", JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION) {

            JOptionPane.showMessageDialog(this, "¡Gracias por su compra!");

        }

    }//GEN-LAST:event_btnPagarActionPerformed

    private void btnAgregarCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCarritoActionPerformed
        // TODO add your handling code here:
        int selectedRow = productosTable.getSelectedRow();
        if (selectedRow != -1) {
            
            String idProducto = productosTable.getValueAt(selectedRow, 0).toString();
            String nombreProducto = productosTable.getValueAt(selectedRow, 1).toString();
            double precioProducto = Double.parseDouble(productosTable.getValueAt(selectedRow, 2).toString());

            
            int cantidad = (int) cantidadSpinner.getValue();

            
            double total = precioProducto * cantidad;

            
            DefaultTableModel carritoModel = (DefaultTableModel) carritoTable.getModel();
            Object[] filaCarrito = {idProducto, nombreProducto, precioProducto, cantidad, total};
            carritoModel.addRow(filaCarrito);

           
            cantidadSpinner.setValue(1);
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un producto para agregar al carrito.");
            
        }
    }//GEN-LAST:event_btnAgregarCarritoActionPerformed

